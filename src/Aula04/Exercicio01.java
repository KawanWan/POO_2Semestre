    private void btnApagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApagarActionPerformed
        String textoApagar = txtApagar.getText();

        int numeroApagar = Integer.parseInt(textoApagar);

        for (int i = 0; i < lstApolices.size(); i++) {
            Apolice apolice = lstApolices.get(i);
            if (apolice.getNumero() == numeroApagar) {
                lstApolices.remove(i);
                JOptionPane.showMessageDialog(rootPane, "Removido com sucesso!");
            }
        }
    }//GEN-LAST:event_btnApagarActionPerformed

