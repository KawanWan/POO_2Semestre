    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        int numero = Integer.parseInt(txtNumero.getText());
        String nome = txtNome.getText();
        int idade = Integer.parseInt(txtIdade.getText());
        char sexo = txtSexo.getText().charAt(0);
        double valorAutomovel = Double.parseDouble(txtValorAutomovel.getText());

        // Crie uma nova Apolice com os valores inseridos
        apolice = new Apolice(numero, nome, idade, sexo, valorAutomovel);

        lstApolices.add(apolice);

        txtNumero.setText("");
        txtNome.setText("");
        txtIdade.setText("");
        txtSexo.setText("");
        txtValorAutomovel.setText("");
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
        if (apolice == null) {
            JOptionPane.showMessageDialog(rootPane, "Você´precisa cadastrar uma apólice!");
        }

        String str = "";

        for (Apolice apolice : lstApolices) {
            str += "\n=======================\n"
                    + apolice.imprimir();
        }

        JOptionPane.showMessageDialog(rootPane, str);
    }//GEN-LAST:event_btnMostrarActionPerformed

    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        String textoPesquisa = txtPesquisa.getText();

        try {
            int numeroPesquisa = Integer.parseInt(textoPesquisa);

            for (Apolice apolice : lstApolices) {
                if (apolice.getNumero() == numeroPesquisa) {
                    JOptionPane.showMessageDialog(rootPane, apolice.imprimir());
                }
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "Número de apólice inválido. Insira um número válido.");
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnPesquisarActionPerformed

