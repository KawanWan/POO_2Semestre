    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        String nome = txtNome.getText();
        String cpf = txtCPF.getText();
        String crmv = txtCRMV.getText();
        char genero = cbGenero.getSelectedItem().toString().charAt(0);
        double valorConsulta = Double.parseDouble(txtValorConsulta.getText());

        veterinario = new Veterinario(nome, cpf, crmv, genero, valorConsulta);
        lstVeterinarios.add(veterinario);

        txtNome.setText("");
        txtCPF.setText("");
        txtCRMV.setText("");
        cbGenero.setSelectedItem(" ");
        txtValorConsulta.setText("");
    }//GEN-LAST:event_btnCadastrarActionPerformed

