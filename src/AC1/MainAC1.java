    private void btnMostrarVetsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarVetsActionPerformed
        if (veterinario == null) {
            JOptionPane.showMessageDialog(rootPane, "Você´precisa cadastrar um veterinário!");
        } else {
            String str = "";

            for (Veterinario veterinario : lstVeterinarios) {
                str += "\n=======================\n"
                        + veterinario.imprimir();

            }
            JOptionPane.showMessageDialog(rootPane, str);
        }


    }//GEN-LAST:event_btnMostrarVetsActionPerformed

    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed

        try {
            String textoPesquisa = txtPesquisa.getText();

            for (Veterinario veterinario : lstVeterinarios) {
                if (veterinario.getCrmv().equals(textoPesquisa)) {
                    JOptionPane.showMessageDialog(rootPane, veterinario.imprimir());
                } else {
                    JOptionPane.showMessageDialog(rootPane, "CRM não encontrado.");
                }
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "CRMV inválido. Por favor insira um CRMV válido.");
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

    private void btnApagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApagarActionPerformed
        try {
            String textoPesquisa = txtPesquisa.getText();

            for (int i = 0; i < lstVeterinarios.size(); i++) {
                Veterinario veterinario = lstVeterinarios.get(i);
                if (veterinario.getCrmv().equals(textoPesquisa)) {
                    lstVeterinarios.remove(i);
                    JOptionPane.showMessageDialog(rootPane, veterinario.getNome() + " foi apagado com sucesso!");
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Número de CRMV não encontrado.");
                }
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "CRMV inválido. Por favor insira um CRMV válido.");
        }
    }//GEN-LAST:event_btnApagarActionPerformed

    private void btnPPagamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPPagamentoActionPerformed
        try {
            String textoPPagamento = txtPPagamento.getText();

            for (Veterinario veterinario : lstVeterinarios) {
                if (veterinario.getCrmv().equals(textoPPagamento)) {
                    labelVeterinario.setText(veterinario.getNome());
                }
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "CRMV inválido. Por favor insira um CRMV válido.");
        }
    }//GEN-LAST:event_btnPPagamentoActionPerformed

